package com.example.firstapp;

public class PlotList
{
    private Integer seasoncode;
    private Integer plotnumber;
    private String villagename;
    private String farmername;
    private String gatsurveno;
    private Integer inareaoutarea;
    private String variety;

    public Integer getSeasoncode() {
        return seasoncode;
    }

    public void setSeasoncode(Integer seasoncode) {
        this.seasoncode = seasoncode;
    }

    public Integer getPlotnumber() {
        return plotnumber;
    }

    public void setPlotnumber(Integer plotnumber) {
        this.plotnumber = plotnumber;
    }

    public String getVillagename() {
        return villagename;
    }

    public void setVillagename(String villagename) {
        this.villagename = villagename;
    }

    public String getFarmername() {
        return farmername;
    }

    public void setFarmername(String farmername) {
        this.farmername = farmername;
    }

    public String getGatsurveno() {
        return gatsurveno;
    }

    public void setGatsurveno(String gatsurveno) {
        this.gatsurveno = gatsurveno;
    }

    public Integer getInareaoutarea() {
        return inareaoutarea;
    }

    public void setInareaoutarea(Integer inareaoutarea) {
        this.inareaoutarea = inareaoutarea;
    }

    public String getVariety() {
        return variety;
    }

    public void setVariety(String variety) {
        this.variety = variety;
    }

}
