package com.example.firstapp;

public class Vehiclefieldsliplist
{
    private Integer vehiclecode;
    private String vehiclenumber;
    private Integer vehiclecategorycode;
    private String vehiclecategory;
    private Integer slipcount;

    public Integer getVehiclecode() {
        return vehiclecode;
    }

    public void setVehiclecode(Integer vehiclecode) {
        this.vehiclecode = vehiclecode;
    }

    public String getVehiclenumber() {
        return vehiclenumber;
    }

    public void setVehiclenumber(String vehiclenumber) {
        this.vehiclenumber = vehiclenumber;
    }

    public Integer getVehiclecategorycode() {
        return vehiclecategorycode;
    }

    public void setVehiclecategorycode(Integer vehiclecategorycode) {
        this.vehiclecategorycode = vehiclecategorycode;
    }


    public String getVehiclecategory() {
        return vehiclecategory;
    }

    public void setVehiclecategory(String vehiclecategory) {
        this.vehiclecategory = vehiclecategory;
    }

    public Integer getSlipcount() {
        return slipcount;
    }

    public void setSlipcount(Integer slipcount) {
        this.slipcount = slipcount;
    }

}
